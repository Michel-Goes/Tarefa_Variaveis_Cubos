// === operador de comparação de igualdade
// !== operador de comparação de diferença
// < operador de comparação de menor que
// > operador de comparação de maior que
// >= operador de comparação de maior ou igual
// <= operador de comparação de menor ou igual 

let numero_1 = 12
let numero_2 = 15

let comparacao = numero_1 === numero_2
comparacao = numero_1 !== numero_2
comparacao = numero_1 < numero_2
comparacao = numero_1 > numero_2
comparacao = numero_1 >= numero_2
comparacao = numero_1 <= numero_2

console.log(comparacao)